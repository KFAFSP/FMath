namespace FMath.Arithmetics
{
    /// <summary>
    /// Enumeration that represents the different types of numerals.
    /// </summary>
    public enum NumeralType
    {
        /// <summary>
        /// Natural numbers.
        /// </summary>
        Natural = 0,

        /// <summary>
        /// Integer numbers.
        /// </summary>
        Integer = 1,

        /// <summary>
        /// Fractional numbers.
        /// </summary>
        Fraction = 2,

        /// <summary>
        /// Real numbers.
        /// </summary>
        Real = 3,

        /// <summary>
        /// Complex numbers.
        /// </summary>
        Complex = 4,

        /// <summary>
        /// Quarternions.
        /// </summary>
        Quarternion = 5
    }
}
